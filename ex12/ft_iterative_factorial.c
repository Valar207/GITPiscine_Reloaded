int		ft_iterative_factorial(int nb)
{
	int		res;
	int		n;

	n = nb;
	res = 1;
	if (nb >= 0 && nb < 13)
	{
		while (n > 1)
		{
			res = res * n;
			n--;
		}
	}
	else
		return (0);
	return (res);
}
